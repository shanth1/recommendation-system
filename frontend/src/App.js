const App = () => {
	const [product, setProduct] = useState(null);
	const [count, setCount] = useState(0);
	const userVector =
		JSON.parse(localStorage.getItem("userVector")) || defaultUserVector;

	const fetchImage = async (imageName) => {
		try {
			const response = await fetch(
				`http://localhost:4000/image?name=${imageName}`,
			);
			const blob = await response.blob();
			const url = URL.createObjectURL(blob);
			document.getElementById("myImage").src = url;
		} catch (error) {
			console.error("Error fetching the image:", error);
		}
	};

	const fetchProduct = async () => {
		try {
			const response = await axios.get(
				"http://localhost:4000/random-product",
			);
			await fetchImage(response.data.product.image_name);
			setProduct(response.data.product);
		} catch (error) {
			console.error("Ошибка при получении продукта:", error);
		}
	};

	const updateFeedback = async (feedback) => {
		try {
			const response = await axios.post(
				"http://localhost:4000/update-vector",
				{
					user_vector: userVector,
					product_id: product?.id,
					feedback: feedback,
				},
			);
			localStorage.setItem(
				"userVector",
				JSON.stringify(response.data.user_vector),
			);
			setProduct(response.data.product);
		} catch (error) {
			console.error("Ошибка при отправке отзыва:", error);
		}
	};

	const resetLocalStorage = () => {
		localStorage.setItem("userVector", JSON.stringify(defaultUserVector));
		fetchProduct();
	};

	useEffect(() => {
		fetchProduct();
	}, []);

	console.log(product);

	return (
		<Container
			style={{
				background: "linear-gradient(white, lightgrey)",
				minHeight: "100vh",
				padding: "20px",
			}}
		>
			<Box
				display="flex"
				justifyContent="space-between"
				alignItems="center"
			>
				<IconButton onClick={resetLocalStorage}>
					<RefreshIcon />
				</IconButton>
				<IconButton>
					<ShoppingCartIcon />
					<Typography>{count}</Typography>
				</IconButton>
			</Box>
			{product ? (
				<Box textAlign="center">
					<Typography variant="h5">{product.name}</Typography>
					<img
						id="myImage"
						src={product.image}
						alt={product.name}
						style={{ maxWidth: "100%" }}
					/>
					<Typography>{product.description}</Typography>
					<Box display="flex" justifyContent="center" mt={2}>
						<IconButton onClick={() => updateFeedback(-1)}>
							<ThumbDownIcon />
						</IconButton>
						<IconButton onClick={() => setCount(count + 1)}>
							<ShoppingCartIcon />
						</IconButton>
						<IconButton onClick={() => updateFeedback(1)}>
							<ThumbUpIcon />
						</IconButton>
					</Box>
				</Box>
			) : (
				<Box textAlign="center">
					<Typography>no products ): </Typography>
				</Box>
			)}
		</Container>
	);
};

export default App;
